void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_USB_PCD_Init(void);

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_PCD_Init();

  void spinLights() {
      GPIOE->ODR ^= GPIO_PIN_8; //LD4
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_9; //LD3
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_10;
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_11;
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_12;
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_13;
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_14;
      HAL_Delay(100);
      GPIOE->ODR ^= GPIO_PIN_15;
      HAL_Delay(100);

      GPIOE->ODR ^= GPIO_PIN_All;
      HAL_Delay(100);
      HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_All);
      HAL_Delay(100);
  }

  while (1)
  {
    if(HAL_GPIO_ReadPin(GPIOA, B1_Pin) == GPIO_PIN_SET) {
    	spinLights();
    }
  }
}

